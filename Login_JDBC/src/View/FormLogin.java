    private void btLogin1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLogin1ActionPerformed
        // TODO add your handling code here:
        lblWarn1.setText("");
        String email = txtEmail1.getText(), password = txtPassword1.getText();
        if(email.isEmpty()){
            txtEmail1.putClientProperty("FlatLaf.style", "arc:20; borderColor:#FF3333; focusedBorderColor:#99FFFF; background:#F0F8FF;");
            return;
        }
        if(password.isEmpty()){
            txtPassword1.putClientProperty("FlatLaf.style", "arc:20; borderColor:#FF3333; focusedBorderColor:#99FFFF; background:#F0F8FF;");
            return;
        }
        if(!controller.checkValidAccount(email, password)){
            lblWarn1.setText("Wrong Email Or Password");
            Timer clearWarning = new Timer(1000, e -> lblWarn1.setText(""));
            clearWarning.setRepeats(false);
            clearWarning.start();
            return;
        }
        // JOptionPane.showMessageDialog(this, "login successfully", "", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();
        new FormCustomer(email).setVisible(true);
         
    }//GEN-LAST:event_btLogin1ActionPerformed

